                            <h4>Water Sources</h4>
                            <p> Calabanga is bounded by the San Miguel Bay on the northern part and the Bicol
                                River on the western part, which are sources of abundant supply of fish of
                                various species and other marine products. The Municipality of Calabanga is
                                endowed with abundant natural resources. The major river systems, the Tigman,
                                Hinaguianan and Inarihan Rivers are presently utilized for irrigation purposes.
                                They originate their main tributaries from Mt. Isarog and flow down in the
                                northwestern direction ultimately discharging to San Miguel Bay. Calabanga is
                                also blessed with many natural spring water resources found in the eastern
                                portion of the municipality along the northwestern flanks of Mt. Isarog. Most of
                                these are found to be potential sources for potable water supply of the
                                municipality, aside from the Hamislag and Tawang Rivers.
