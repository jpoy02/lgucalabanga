                            <h4>Topography</h4>
                            <p>
                                The topographic landscape of the municipality could be characterized as
                                generally flat with a slope 0 - 3%, gently sloping (3 - 8%) towards the
                                direction of the eastern portion and finally, rolling up to higher steep towards
                                the direction of the southeastern part going to Mt. Isarog. The 0 - 3% slope is
                                a broad area of flat to nearly level land which extends from Barangay Balongay
                                along the Bicol River to Poblacion Area and surrounding areas down to Barangay
                                Manguiring. This is the dominant slope of the municipality.
                            </p>
