                            <h4>Soil / Land</h4>
                            <p>There are six soil types that could be found in the municipality. These are the
                                (1) Hydrosol which is dominant along the Bicol River covering parts of Barangay
                                Balongay, San Bernardino and Punta Tarawal; (2) Balong Clay; (3) Pili Clay Loam;
                                (4) Tigaon Clay which covers the poblacion; (5) Annan Clay Loam which could be
                                found towards the direction of Tinambac and (6) Mountain Soil in the Mt. Isarog
                                area</p>
